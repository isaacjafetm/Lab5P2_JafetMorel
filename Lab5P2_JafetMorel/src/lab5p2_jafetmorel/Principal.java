    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < usuarios.size(); i++) {
            if (((Alumnos) usuarios.get(i)).equals(al)) {
                ((Alumnos) usuarios.get(i)).setUsuario(tf_alus.getText());
                ((Alumnos) usuarios.get(i)).setContraseña(tf_alpw.getText());
                ((Alumnos) usuarios.get(i)).setNombre(tf_alnom.getText());
                ((Alumnos) usuarios.get(i)).setApellido(tf_alap.getText());
                ((Alumnos) usuarios.get(i)).setCuenta(Integer.parseInt(tf_alid.getText()));
                ((Alumnos) usuarios.get(i)).setClases((int) sp_alclases.getValue());
                ((Alumnos) usuarios.get(i)).setAño((int) sp_alaño.getValue());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jt_decMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_decMouseClicked
        // TODO add your handling code here:
        if (evt.isMetaDown()) {
            //seleccionar un nodo con click derecho
            int row = jt_dec.getClosestRowForLocation(
                    evt.getX(), evt.getY());
            jt_dec.setSelectionRow(row);
            Object v1
                    = jt_dec.getSelectionPath().
                            getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;
            if (nodo_seleccionado.getUserObject() instanceof Persona) {
                persona_seleccionada
                        = (Persona) nodo_seleccionado.
                                getUserObject();
                popup.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }

        }
    }//GEN-LAST:event_jt_decMouseClicked

    private void elMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_elMouseClicked
        // TODO add your handling code here:
        DefaultTreeModel m
                = (DefaultTreeModel) jt_dec.getModel();
        m.removeNodeFromParent(
                nodo_seleccionado);
        m.reload();
        usuarios.remove((Persona)nodo_seleccionado.getUserObject());
        
    }//GEN-LAST:event_elMouseClicked

