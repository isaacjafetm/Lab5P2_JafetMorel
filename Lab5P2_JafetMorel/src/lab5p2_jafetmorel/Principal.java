    private void jb_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_loginActionPerformed
        // TODO add your handling code here:
        if (usuarios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay ningún usuario ingresado.");
        } else {
            int aux = 0;
            for (int i = 0; i < usuarios.size(); i++) {
                if (usuarios.get(i).getUsuario().equals(tf_loginus.getText()) & usuarios.get(i).getContraseña().equals(tf_loginpw.getText())) {
                    if (usuarios.get(i) instanceof CEO) {
                        aux++;

                        break;
                    } else if (usuarios.get(i) instanceof Decano) {
                        aux++;
                        DefaultTreeModel model = (DefaultTreeModel) jt_dec.getModel();
                        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) model.getRoot();
                        for (int j = 0; j < usuarios.size(); j++) {
                            if (usuarios.get(j) instanceof Docente) {
                                DefaultMutableTreeNode docente = new DefaultMutableTreeNode(usuarios.get(j));
                                for (int k = 0; k < ((Docente) usuarios.get(j)).getAlumnos().size(); k++) {
                                    DefaultMutableTreeNode alumno = new DefaultMutableTreeNode(((Docente) usuarios.get(j)).getAlumnos().get(i));
                                    docente.add(alumno);
                                }
                                raiz.add(docente);
                            }
                        }
                        model.reload();
                        jt_dec.setModel(model);
                        mostrardec();
                        break;
                    } else if (usuarios.get(i) instanceof Docente) {
                        aux++;
                        DefaultListModel modelo = (DefaultListModel) lista_doc.getModel();
                        for (int j = 0; j < usuarios.size(); j++) {
                            if (usuarios.get(j) instanceof Alumnos) {
                                modelo.addElement(usuarios.get(j));
                            }
                        }
                        lista_doc.setModel(modelo);
                        doc = (Docente) usuarios.get(i);
                        mostrardocente();
                        break;
                    } else if (usuarios.get(i) instanceof Alumnos) {
                        aux++;
                        al = (Alumnos) usuarios.get(i);
                        DefaultTableModel model = (DefaultTableModel) table_pruebas.getModel();
                        model.setRowCount(0);
                        for (int j = 0; j < al.getPruebas().size(); j++) {
                            Object[] row = {al.getPruebas().get(j).getEquipo(), al.getPruebas().get(j).getCoop(), al.getPruebas().get(j).getParticipacion(), al.getPruebas().get(j).getNota(), al.getPruebas().get(j).getGeneral(), al.getPruebas().get(j).getDocente()};
                            model.addRow(row);
                        }
                        table_pruebas.setModel(model);
                        mostraral();
                        break;
                    }
                }
            }
            if (aux == 0) {
                JOptionPane.showMessageDialog(this, "Nombre de usuario y/o contraseña incorrecta.");
            }
        }
        tf_loginus.setText("");
        tf_loginpw.setText("");
    }//GEN-LAST:event_jb_loginActionPerformed

